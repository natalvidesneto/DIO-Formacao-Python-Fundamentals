# Operadores de atribuição

# Atribuição simples (=)
saldo = 500
print(saldo)

# Atribuição com adição (+=)
saldo += 100
print(saldo)

# Atribuição com subtração(-+)
saldo -= 200
print(saldo)


# Atrubuição com multiplicação (*=)
saldo *= 100
print(saldo)

# Atribuição com divisão
saldo /= 10
print(saldo)

# Atriução com divisão inteira (//=)
valor = 100
valor //= 5
print(valor)

# Atribuição com módulo
saldo2 = 100
saldo2 %= 2
print(saldo2)


# Atribuição com exponenciação
saldo3 = 100
saldo3 ** 5
print(saldo3)